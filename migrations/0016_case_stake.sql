-- Кейсы переходят с фиксированной цены на ВАРИАТИВНУЮ ставку: игрок ставит любую сумму
-- и крутит, приз = ставка × множитель (как в Костях/Ракете). Лента теперь показывает
-- множители (×0.5 … ×200), а не абсолютные суммы TON — поэтому никаких ограничений по
-- размеру ставки. Колонка price_nano семантически стала «ставкой» — переименовываем для
-- ясности (тест-фаза, данных почти нет).
ALTER TABLE case_spins RENAME COLUMN price_nano TO stake_nano;
